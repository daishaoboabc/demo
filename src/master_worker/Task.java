package master_worker;

public class Task {
    
    
    
    private int id;
    private int jg;
    private String name;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getJg() {
        return jg;
    }
    public void setJg(int jg) {
        this.jg = jg;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    
    
}
